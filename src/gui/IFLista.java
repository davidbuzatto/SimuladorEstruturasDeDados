    private void btnRemoverFimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverFimActionPerformed
        
        try {
            fieldRemoveuInicio.setText( "" );
            fieldRemoveuFim.setText( lista.removerFim() );
            if ( !lista.estaVazia() ) {
                fieldInicio.setText( lista.consultarInicio() );
                fieldFim.setText( lista.consultarFim() );
            } else {
                fieldInicio.setText( "vazio" );
                fieldFim.setText( "vazio" );
            }
            fieldTamanho.setText( String.valueOf( lista.getTamanho() ) );
            painelDesenho.repaint();
        } catch ( EmptyListException exc ) {
            fieldRemoveuInicio.setText( "" );
            fieldRemoveuFim.setText( "" );
            fieldInicio.setText( "vazio" );
            fieldFim.setText( "vazio" );
            fieldTamanho.setText( String.valueOf( lista.getTamanho() ) );
            painelDesenho.repaint();
            JOptionPane.showMessageDialog( this, "Lista vazia.", 
                    "Aviso", JOptionPane.WARNING_MESSAGE );
        }
        
    }//GEN-LAST:event_btnRemoverFimActionPerformed

