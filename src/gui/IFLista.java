    private void itemMenuRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_itemMenuRemoverActionPerformed
        
        String valor = lista.remover( item.getIndice() );
        
        fieldRemoveuInicio.setText( "" );
        fieldRemoveuFim.setText( "" );
        
        if ( !lista.estaVazia() ) {
            fieldInicio.setText( lista.consultarInicio() );
            fieldFim.setText( lista.consultarFim() );
        } else {
            fieldInicio.setText( "vazio" );
            fieldFim.setText( "vazio" );
        }
        
        fieldTamanho.setText( String.valueOf( lista.getTamanho() ) );
        painelDesenho.repaint();
        
        JOptionPane.showMessageDialog( painelDesenho, 
                String.format( "O item \"%s\" foi removido da posicao %d.",
                valor, item.getIndice() ), "Removido", 
                JOptionPane.INFORMATION_MESSAGE );
        
    }//GEN-LAST:event_itemMenuRemoverActionPerformed

