    private void painelDesenhoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_painelDesenhoMouseClicked
        
        if ( SwingUtilities.isRightMouseButton( evt ) ) {

            List<AreaItemLista> areasItens = listaD.getAreasItens();

            for ( AreaItemLista item : areasItens ) {
                
                Point p = evt.getPoint();
                
                if ( item.getArea().contains( p ) ) {
                    
                    this.item = item;
                    
                    itemMenuInserirPosicao.setText( String.format( "Inserir Aqui (posicao: [%d])", item.getIndice() ) );
                    itemMenuInserirDireita.setText( String.format( "Inserir à Direita (posicao: [%d])", item.getIndice() + 1 ) );
                    itemMenuRemover.setText( String.format( "Remover (posicao: [%d])", item.getIndice() ) );
                    
                    menuPopUp.show( painelDesenho, p.x, p.y );
                    break;
                    
                }
            }
            
        }
        
        painelDesenho.repaint();
        
    }//GEN-LAST:event_painelDesenhoMouseClicked

