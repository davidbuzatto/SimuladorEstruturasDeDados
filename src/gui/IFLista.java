    private void inserirEmIndice( int indice ) {
        
        String valor = JOptionPane.showInputDialog( painelDesenho, 
                String.format( "Valor a ser inserido na posição [%d]:", indice ) );

        if ( valor != null ) {

            lista.inserir( valor, indice );

            fieldValorInicio.setText( "" );
            fieldValorFim.setText( "" );
            fieldRemoveuInicio.setText( "" );
            fieldRemoveuFim.setText( "" );
            fieldInicio.setText( lista.consultarInicio() );
            fieldFim.setText( lista.consultarFim() );
            fieldTamanho.setText( String.valueOf( lista.getTamanho() ) );

            painelDesenho.repaint();
            item = null;

        }
        
    }
    
